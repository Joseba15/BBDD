CREATE OR REPLACE
FUNCTION NOTA(NOTA NUMBER)
RETURN VARCHAR2
IS
    RESULT VARCHAR2(60);
    BEGIN
        IF NOTA<5 THEN
            RESULT:='Es un insuficiente';
        ELSIF NOTA=5 THEN
            RESULT:='Es un suficiente';
        ELSIF NOTA=6 THEN
            RESULT:='Es un bien';
        ElSIF NOTA=7 OR NOTA=8  THEN
            RESULT:='Es un notable';
        ElSIF NOTA=9 OR NOTA=10  THEN
            RESULT:='Es un sobresaliente';
        END IF;
        RETURN RESULT;
    END;

