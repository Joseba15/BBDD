CREATE OR REPLACE
FUNCTION AZAR(MINIMO NUMBER, MAXIMO  NUMBER)
RETURN NUMBER
AS
    BEGIN
        RETURN MOD(ABS(DBMS_RANDOM.RANDOM),MAXIMO)+MINIMO;
    END;


