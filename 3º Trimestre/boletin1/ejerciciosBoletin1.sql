CREATE OR REPLACE
PROCEDURE SERIE(MINIMO NUMBER, MAXIMO  NUMBER,PASO NUMBER)
AS
    CONTADOR NUMBER;
    BEGIN
        CONTADOR:=MINIMO;
        WHILE CONTADOR <=MAXIMO LOOP
            DBMS_OUTPUT.PUT_LINE(CONTADOR);
            CONTADOR:=CONTADOR+PASO;
            END LOOP;
    END;
